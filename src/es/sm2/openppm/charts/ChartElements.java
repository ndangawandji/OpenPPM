package es.sm2.openppm.charts;


public class ChartElements {
	private String name;
	private String value;
	
	public ChartElements() {
		super();
	}

	public ChartElements(String name, String value) {
		this.name = name;
		this.value = value;
	}

	/**
	 * @return
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return
	 */
	public String getValue() {
		return value;
	}

	/**
	 * @param value
	 */
	public void setValue(String value) {
		this.value = value;
	}

}
